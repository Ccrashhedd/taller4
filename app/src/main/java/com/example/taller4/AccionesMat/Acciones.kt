package com.example.taller4.AccionesMat



class Acciones {

    fun sumar(a: Float, b: Float): Float {
        return a + b
    }

    fun restar(a: Float, b:Float): Float {
        return a - b
    }

    fun multiplicacion (a: Float, b: Float): Float {
        return a * b
    }

    fun division (a: Float, b: Float): Float {
        return a / b
    }

}